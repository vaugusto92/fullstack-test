/**
 * Class that represents the Track object
 * from the SpringBoot application.
 * 
 * Author: Victor Augusto Alves Catanante
 */
export class Track {
    /**
     * The name of the track.
     */
    name?: string;
    
    /**
     * An array with the name of the artists.
     */
    artists: string[];
    
    /**
     * The string representation of the names of artists.
     */
    artistList?: string;
}